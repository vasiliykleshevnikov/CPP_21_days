#include <iostream>

int main()
{
	using std::cout;
	using std::endl;

	unsigned short int width = 5;
	unsigned short length = 10;

	unsigned short int area = (width * length);

	cout << "Width: " << width << " \n";
	cout << "Length: " << length << endl;
	cout << "Area: " << area << endl;

	return 0;
}
